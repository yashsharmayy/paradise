function applyLanguage(lang) {
  const t = translations[lang];
  if (!t) return;

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const keys = el.getAttribute("data-i18n").split(".");
    let value = t;
    for (let key of keys) {
      value = value?.[key];
    }
    if (value) {
      if (el.hasAttribute("data-html")) {
        el.innerHTML = value;
        } else {
        el.textContent = value;
        }

    }
  });

  const langBtnText = document.getElementById("langText");
  if (langBtnText) {
    langBtnText.textContent = lang === "english" ? "हिंदी" : "English";
  }

  // Store current language in localStorage
  localStorage.setItem("language", lang);
}

// On language button click
document.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("language") || "english";
  applyLanguage(savedLang);

  const langBtn = document.getElementById("toggleLang");
  if (langBtn) {
    langBtn.addEventListener("click", () => {
      const current = localStorage.getItem("language") || "english";
      const newLang = current === "english" ? "hindi" : "english";
      applyLanguage(newLang);
    });
  }
});


